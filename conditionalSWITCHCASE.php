<?php

$daysOfWeek = 7;

switch ($daysOfWeek) {
    case 1:
        echo "sunday";
        break;
    case 2:
        echo "monday";
        break;
    case 3: 
        echo "tuesday";
        break;
    case 4:
        echo "wednesday";
        break;
    case 5:
        echo "thursday";
        break;
    case 6: 
        echo "friday";
        break;
    case 7:
        echo "saturday";
        break;
    default:
        echo "error";
        break;
}