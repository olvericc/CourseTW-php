<?php

// comparison

$a = 10;
$b = 20;

// operator ==
var_dump($a == $b);
echo "<br>";

// operator <> or !=
var_dump($a <> $b); # or use var_dump($a != $b);
echo "<br>";

// operator >
var_dump($a > $b);
echo "<br>";

// operator <
var_dump($a < $b);
echo "<br>";

// operator >=
var_dump($a >= $b);
echo "<br>";

// operator <=
var_dump($a <= $b);
echo "<br>";

// operator ===
var_dump($a === $b);
echo "<br>";

// operator !==
var_dump($a !== $b);
echo "<br>";